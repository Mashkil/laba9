#include <stdio.h>

int main() {
	int A = 7; // Задаем стороны прямоугольника и квадрата.
	int B = 8;
	int C = 6;
	int N = ((A / C) * (B / C)); // Рассчитываем кол-во квадратов в прямоугольнике.
	int S = (A * B - N * C * C); // Рассчитываем оставшуюся площадь.
	printf_s("%i\n", N); // Выводим результаты
	printf_s("%i", S);
	return 0;
}
